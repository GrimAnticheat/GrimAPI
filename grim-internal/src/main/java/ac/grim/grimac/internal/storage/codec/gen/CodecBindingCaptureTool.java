package ac.grim.grimac.internal.storage.codec.gen;

import ac.grim.grimac.api.storage.codec.EncodeShape;
import ac.grim.grimac.api.storage.codec.EncodeShape.FieldDef;
import ac.grim.grimac.api.storage.codec.Persistent;
import ac.grim.grimac.internal.storage.codec.CodecIntrospection;
import ac.grim.grimac.internal.storage.codec.RecordLayout;

import java.io.File;
import java.io.IOException;
import java.io.Writer;
import java.nio.charset.StandardCharsets;
import java.nio.file.Files;
import java.util.ArrayList;
import java.util.List;
import java.util.TreeSet;
import java.util.jar.JarEntry;
import java.util.jar.JarFile;

/**
 * Build-time only. Runs the <em>real</em> {@link CodecIntrospection#inspect}
 * and {@link RecordLayout#fromReflection} on the pre-obfuscation builtin
 * records and writes their resolved shape + layout to a TSV resource that
 * {@link ac.grim.grimac.internal.storage.codec.CapturedBindings} reads at
 * runtime. This removes the codec's dependence on the {@code RecordComponents}
 * class attribute, which Allatori strips — see {@code CapturedBindings} for
 * the full rationale.
 * <p>
 * Invoked by the {@code generateCodecBindings} Gradle task with one argument:
 * the output file path. Never called at runtime; loading it in an obfuscated
 * jar would fail (it reflects), which is fine because it is never invoked there.
 * <p>
 * Discovery is scoped to {@value #MODEL_PACKAGE_PATH}: the convention is that
 * every builtin {@code @Persistent} record lives in that api package (so its
 * class name, accessors and field types stay in the obf keep-set and resolve
 * by their original names at runtime). A record placed outside it would not be
 * captured and would fall back to reflection — which fails under obfuscation.
 */
public final class CodecBindingCaptureTool {

    private static final String MODEL_PACKAGE_PATH = "ac/grim/grimac/api/storage/model/";

    private CodecBindingCaptureTool() {}

    public static void main(String[] args) throws Exception {
        if (args.length < 1) {
            throw new IllegalArgumentException("usage: CodecBindingCaptureTool <output-file>");
        }
        File out = new File(args[0]);
        List<Class<?>> records = discoverRecords();
        if (records.isEmpty()) {
            // Hard failure: an empty resource silently disables the obf-safe path,
            // which only surfaces as a runtime crash in the obfuscated jar.
            throw new IllegalStateException(
                "no @Persistent records found under " + MODEL_PACKAGE_PATH
                    + " on the capture classpath — codec bindings would be empty");
        }

        StringBuilder sb = new StringBuilder(4096);
        sb.append("# Generated by CodecBindingCaptureTool. Do not edit.\n");
        sb.append("# Build-time-captured v2 codec shape + layout per @Persistent record;\n");
        sb.append("# read by CapturedBindings so the codec needs no RecordComponents at runtime.\n\n");
        for (Class<?> rec : records) {
            // Force the reflective path — capture must derive from the live
            // record, never from a (possibly stale) captured resource.
            EncodeShape shape = CodecIntrospection.inspectReflective(rec);
            RecordLayout layout = RecordLayout.fromReflection(rec, shape);
            writeRecord(sb, rec, shape, layout);
        }

        File parent = out.getParentFile();
        if (parent != null) Files.createDirectories(parent.toPath());
        try (Writer w = Files.newBufferedWriter(out.toPath(), StandardCharsets.UTF_8)) {
            w.write(sb.toString());
        }
        System.out.println("[codec-bindings] captured " + records.size() + " record(s) -> " + out);
        for (Class<?> rec : records) System.out.println("[codec-bindings]   " + rec.getName());
    }

    private static void writeRecord(StringBuilder sb, Class<?> rec, EncodeShape shape, RecordLayout layout) {
        sb.append("R\t").append(rec.getName())
          .append('\t').append(shape.totalComponentCount())
          .append('\t').append(layout.version())
          .append('\t').append(typeNames(layout.ctorParamTypes()))
          .append('\n');
        sb.append("S\t").append(shape.idField())
          .append('\t').append(shape.timestampField() == null ? "" : shape.timestampField())
          .append('\t').append(String.join(",", shape.partitionFields()))
          .append('\t').append(String.join(",", shape.indexedFields()))
          .append('\t').append(String.join(",", shape.searchableFields()))
          .append('\n');
        List<FieldDef> fields = shape.fields();
        for (int i = 0; i < fields.size(); i++) {
            FieldDef f = fields.get(i);
            sb.append("F\t").append(f.name())
              .append('\t').append(typeName(f.javaType()))
              .append('\t').append(f.kind())
              .append('\t').append(f.nullable())
              .append('\t').append(f.searchType() == null ? "" : f.searchType())
              .append('\t').append(f.vectorDimension())
              .append('\t').append(f.recordIndex())
              .append('\t').append(f.mergeMode())
              .append('\t').append(f.sentinelValue())
              .append('\t').append(layout.accessorNames()[i])
              .append('\n');
        }
        sb.append('\n');
    }

    private static String typeNames(Class<?>[] types) {
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < types.length; i++) {
            if (i > 0) sb.append(',');
            sb.append(typeName(types[i]));
        }
        return sb.toString();
    }

    /** Inverse of {@code CapturedBindings.resolveType}: readable, stable type names. */
    private static String typeName(Class<?> t) {
        if (t == byte[].class) return "byte[]";
        if (t == float[].class) return "float[]";
        // Any other array type is unsupported by the codec (TypeTag.of would reject it);
        // fail loudly rather than emit something resolveType can't read back.
        if (t.isArray()) throw new IllegalArgumentException("unsupported codec field array type: " + t.getName());
        return t.getName(); // primitives -> "int"/"long"/...; references -> FQCN
    }

    /** Scan the capture classpath for {@code @Persistent} records in the model package. */
    private static List<Class<?>> discoverRecords() throws IOException, ClassNotFoundException {
        TreeSet<String> fqcns = new TreeSet<>();
        String classpath = System.getProperty("java.class.path", "");
        for (String entry : classpath.split(File.pathSeparator)) {
            if (entry.isEmpty()) continue;
            File f = new File(entry);
            if (f.isDirectory()) {
                File pkgDir = new File(f, MODEL_PACKAGE_PATH);
                File[] classFiles = pkgDir.listFiles((d, n) -> n.endsWith(".class"));
                if (classFiles != null) {
                    for (File c : classFiles) fqcns.add(toFqcn(MODEL_PACKAGE_PATH + c.getName()));
                }
            } else if (f.isFile() && entry.endsWith(".jar")) {
                try (JarFile jar = new JarFile(f)) {
                    var it = jar.entries();
                    while (it.hasMoreElements()) {
                        JarEntry e = it.nextElement();
                        String n = e.getName();
                        if (n.startsWith(MODEL_PACKAGE_PATH) && n.endsWith(".class")
                                && n.indexOf('/', MODEL_PACKAGE_PATH.length()) < 0) {
                            fqcns.add(toFqcn(n));
                        }
                    }
                }
            }
        }

        List<Class<?>> records = new ArrayList<>();
        ClassLoader cl = CodecBindingCaptureTool.class.getClassLoader();
        for (String fqcn : fqcns) {
            Class<?> c = Class.forName(fqcn, false, cl);
            if (c.isRecord() && c.isAnnotationPresent(Persistent.class)) records.add(c);
        }
        return records;
    }

    private static String toFqcn(String classFilePath) {
        return classFilePath.substring(0, classFilePath.length() - ".class".length()).replace('/', '.');
    }
}
